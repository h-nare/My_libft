#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	int		length;
	int		i;

	i = 0;
	length = ft_strlen(s);
	dup = (char *)malloc((length + 1) * sizeof(char));
	if (!dup)
		return (NULL);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
